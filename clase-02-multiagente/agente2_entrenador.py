"""## Paso 4 — Regresion Logistica

Modelo lineal simple. Sirve como linea base: si no supera esto,
los modelos mas complejos tienen algun problema.

Se evalua con **validacion cruzada estratificada de 5 folds** para medir
estabilidad (no solo rendimiento en una particion).
"""

from sklearn.linear_model import LogisticRegression
from sklearn.model_selection import cross_val_score, StratifiedKFold
from sklearn.metrics import accuracy_score, f1_score, classification_report
import warnings
warnings.filterwarnings('ignore')

resultados = {}

def entrenar_y_evaluar(nombre, modelo, X_train, y_train, X_test, y_test):
    cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)
    cv_scores = cross_val_score(modelo, X_train, y_train, cv=cv, scoring='f1')
    modelo.fit(X_train, y_train)
    y_pred = modelo.predict(X_test)
    return {
        'modelo'      : modelo,
        'accuracy'    : round(accuracy_score(y_test, y_pred), 4),
        'f1'          : round(f1_score(y_test, y_pred), 4),
        'cv_f1_media' : round(cv_scores.mean(), 4),
        'cv_f1_std'   : round(cv_scores.std(), 4),
        'y_pred'      : y_pred,
        'reporte'     : classification_report(y_test, y_pred)
    }

lr = LogisticRegression(max_iter=1000, random_state=42)
resultados['Logistic Regression'] = entrenar_y_evaluar(
    'Logistic Regression', lr, X_train, y_train, X_test, y_test
)
r = resultados['Logistic Regression']
print(f'Logistic Regression')
print(f'  Accuracy:    {r["accuracy"]}')
print(f'  F1-Score:    {r["f1"]}')
print(f'  CV F1 media: {r["cv_f1_media"]} +/- {r["cv_f1_std"]}')
print()
print(r['reporte'])

