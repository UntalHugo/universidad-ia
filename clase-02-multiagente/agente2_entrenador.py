"""## Paso 7 — Transformers (DistilBERT + embeddings)

En lugar de usar DistilBERT como clasificador directo, lo usamos como **extractor de features**:
1. Cada fila del dataset se convierte a una descripcion textual breve
2. DistilBERT produce un vector de 768 dimensiones para cada texto (vector `[CLS]`)
3. Una regresion logistica simple entrena sobre esos vectores

Se trabaja sobre una muestra para que corra en CPU en tiempo razonable.
"""

from transformers import AutoTokenizer, AutoModel
from sklearn.linear_model import LogisticRegression as LR_emb
import torch

MODELO_BERT  = 'distilbert-base-uncased'
MUESTRA_BERT = 600

print('Cargando DistilBERT...')
tokenizador  = AutoTokenizer.from_pretrained(MODELO_BERT)
modelo_bert  = AutoModel.from_pretrained(MODELO_BERT)
print('Modelo cargado.')

def fila_a_texto(fila, columnas):
    """Convierte una fila numerica a una descripcion textual usando los nombres de columna."""
    partes = []
    for col in columnas[:8]:
        partes.append(f'{col} {round(float(fila[col]), 2)}')
    return ' '.join(partes)

def obtener_embeddings(textos, tokenizador, modelo):
    """Extrae el vector [CLS] de cada texto. Es el vector que resume toda la secuencia."""
    embeddings = []
    modelo.eval()
    with torch.no_grad():
        for texto in textos:
            tokens = tokenizador(
                texto,
                return_tensors='pt',
                truncation=True,
                max_length=64,
                padding='max_length'
            )
            salida = modelo(**tokens)
            # last_hidden_state[:, 0, :] es el vector del token [CLS] (posicion 0)
            vector_cls = salida.last_hidden_state[:, 0, :].squeeze().numpy()
            embeddings.append(vector_cls)
    return np.array(embeddings)

columnas = list(X_train.columns)

# Muestra de train
idx_train   = X_train.index[:MUESTRA_BERT]
textos_train = [fila_a_texto(X_train.loc[i], columnas) for i in idx_train]
y_train_mst  = y_train.loc[idx_train]

# Muestra de test
idx_test    = X_test.index[:int(MUESTRA_BERT * 0.25)]
textos_test  = [fila_a_texto(X_test.loc[i], columnas) for i in idx_test]
y_test_mst   = y_test.loc[idx_test]

print(f'Extrayendo embeddings: {len(textos_train)} train / {len(textos_test)} test...')
X_emb_train = obtener_embeddings(textos_train, tokenizador, modelo_bert)
X_emb_test  = obtener_embeddings(textos_test,  tokenizador, modelo_bert)
print(f'Shape embeddings: {X_emb_train.shape}  (cada fila = vector de 768 dims)')

# Entrenar clasificador sobre los embeddings de DistilBERT
clf_emb = LR_emb(max_iter=500, random_state=42)
clf_emb.fit(X_emb_train, y_train_mst)
y_pred_emb = clf_emb.predict(X_emb_test)

resultados['Transformers + LR (embeddings)'] = {
    'modelo'      : clf_emb,
    'accuracy'    : round(accuracy_score(y_test_mst, y_pred_emb), 4),
    'f1'          : round(f1_score(y_test_mst, y_pred_emb, zero_division=0), 4),
    'cv_f1_media' : 'N/A',
    'cv_f1_std'   : 'N/A',
    'y_pred'      : y_pred_emb,
    'reporte'     : classification_report(y_test_mst, y_pred_emb)
}
r = resultados['Transformers + LR (embeddings)']
print(f'Transformers + LR (embeddings)')
print(f'  Accuracy: {r["accuracy"]}')
print(f'  F1-Score: {r["f1"]}  (sobre muestra de {len(y_test_mst)} test)')
print()
print(r['reporte'])

"""## Paso 8 — Comparativa y seleccion del mejor modelo"""

